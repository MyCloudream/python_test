import numpy as np

a = np.floor(10 * np.random.random((12, 2)))

'''
[[ 8.  8.]
 [ 9.  2.]
 [ 3.  0.]
 [ 5.  1.]
 [ 7.  3.]
 [ 7.  4.]
 [ 5.  1.]
 [ 2.  9.]
 [ 1.  3.]
 [ 1.  3.]
 [ 2.  6.]
 [ 9.  3.]]
'''
print(a)

# 竖方向横着分割，a为要分割的矩阵，3表示分割为3份。
'''
[array([[ 8.,  8.],
       [ 9.,  2.],
       [ 3.,  0.],
       [ 5.,  1.]]), array([[ 7.,  3.],
       [ 7.,  4.],
       [ 5.,  1.],
       [ 2.,  9.]]), array([[ 1.,  3.],
       [ 1.,  3.],
       [ 2.,  6.],
       [ 9.,  3.]])]
'''
print(np.vsplit(a, 3))


# 在第三行后面切一刀，在第四行后面切一刀，两刀切成3块
'''
[array([[ 8.,  8.],
       [ 9.,  2.],
       [ 3.,  0.]]), array([[ 5.,  1.]]), array([[ 7.,  3.],
       [ 7.,  4.],
       [ 5.,  1.],
       [ 2.,  9.],
       [ 1.,  3.],
       [ 1.,  3.],
       [ 2.,  6.],
       [ 9.,  3.]])]
'''
print(np.vsplit(a, (3, 4)))
