import numpy as np

# 生成3行4列的二维矩阵，取值为随机的浮点数，在半开区间 [0.0, 1.0)

a = 10 * np.random.random((3, 4))

# 乘10之后，得到类似如下的矩阵：
'''
[[ 4.12913155  3.74225311  5.99355689  6.46899251]
 [ 9.6168953   2.97444891  1.94358161  8.59825219]
 [ 2.55920397  0.88531021  1.97703553  3.21143099]]
'''
print(a)

# floor 用于向下取整
'''
[[ 4.  3.  5.  6.]
 [ 9.  2.  1.  8.]
 [ 2.  0.  1.  3.]]
'''
b = np.floor(a)
print(b)

# 平坦化数组
print(np.ravel(b))
