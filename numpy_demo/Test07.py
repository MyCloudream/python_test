import numpy as np

# 生成3行4列的二维矩阵，取值为随机的浮点数，在半开区间 [0.0, 1.0)

a = np.floor(10 * np.random.random((3, 4)))

'''
[[ 4.  1.  3.  4.]
 [ 0.  2.  4.  3.]
 [ 2.  0.  3.  4.]]
'''
print(a)

# 平坦化数组，其实就是将二维数组转为一维数组，转换规则为从左到右从上到下
b = np.ravel(a)
'''
[ 4.  1.  3.  4.  0.  2.  4.  3.  2.  0.  3.  4.]
'''
print(b)

# 矩阵转换，其实就是将一维数组转为二维数组，当然也可以是一种二维转为另一种二维数组
'''
[[ 4.  1.]
 [ 3.  4.]
 [ 0.  2.]
 [ 4.  3.]
 [ 2.  0.]
 [ 3.  4.]]
'''
print(b.reshape(6, 2))
